use Horus\HorusNode;
use Horus\HorusHiero;
use Horus\HorusHTML;

$BASE_URL = '../';  // Set to URL where images and style sheets can be found
$HEADER   = '';
$FOOTER   = '';

if(count($argv)) {
	$args = array();
	for($i=0; $i<count($argv); ++$i) {
		$a = $argv[$i];
		if(strncmp($a,'-font=',6)===0) $args['font'] = substr($a,6);
		elseif(strncmp($a,'-mcd=',5)===0) $args['mcd'] = substr($a,5);
		elseif(strncmp($a,'-text=',6)===0) $args['text'] = substr($a,6);
		elseif(strncmp($a,'-height=',8)===0) $args['height'] = substr($a,8);
		elseif($a=='-lines') $args['lines'] = true;
	}
} elseif(count($_REQUEST)) {
	$args = &$_REQUEST;
	$HEADER = <<<END
<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<title>Horus Hireoglyphic</title>
<link type="text/css" href="{$BASE_URL}hiero.css" rel="stylesheet" />
</head>
<body>
END;
	$FOOTER = <<<END
</body>
</html>
END;
}

if(isset($args['mdc'])) $text = $args['mdc'];
elseif(isset($args['text'])) $text = $args['text'];
else $text = file_get_contents('php://input');

if(!$text) die;

require_once __DIR__.'/Horus/HorusHiero.php';
require_once __DIR__.'/Horus/HorusHTML.php';
require_once __DIR__.'/Horus/HorusNode.php';

$font    = isset($args['font'])?   $args['font']   : HIERO_DEFAULT_FONT;
$height  = isset($args['height'])? $args['height'] : HIERO_DEFAULT_HEIGHT;
$lines   = isset($args['lines'])?  $args['lines']  : HIERO_DEFAULT_LINES;

require_once __DIR__.'/phonemes.php';
require_once __DIR__."/metrics-{$font}.php";

echo $HEADER;

echo HorusHiero::parse($text,array(
	'font'=>$font,
	'output'=>'html',
	'font-url'=>"{$BASE_URL}{$font}/",
	'height'=>$height,
	'lines'=>$lines,
	'comment'=>'Generated by HorusHiero',
));

echo $FOOTER;